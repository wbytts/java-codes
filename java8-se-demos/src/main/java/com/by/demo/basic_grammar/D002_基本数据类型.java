package com.by.demo.basic_grammar;

public class D002_基本数据类型 {
    public static void main(String[] args) {
        /*
            整数类型：byte、short、int、long
            浮点类型：float、double
            字符类型：char
            布尔类型：boolean
            空类型：void
        */
        byte a = 1;
        short b = 2;
        int c = 3;
        long d = 4L;
        float e = 3.14F;
        double f = 3.1415;
        char g = 'A';
        boolean h = false;
    }
}
